// Create a function that returns true when x is equal to y; otherwise return false.

// Examples
// isSameNum(4, 8) ➞ false

// isSameNum(2, 2) ➞  true

// isSameNum(42, 32) ➞  false

function isSameNum(num1, num2) {
	if (num1 === num2) {
        return true; 
    } else {
        return false; 
    }
}
